//how to reverse a string
public class StringReverse {
	public void reverse() {
		String s="string";
		char[] cr=s.toCharArray();
		//int k=cr.length;
		for(int i=cr.length;i>0;i--) {
			System.out.println();
		}
	}
	

	public static void main(String[] args) {
		StringReverse sr=new StringReverse();
		sr.reverse();

	}

}
