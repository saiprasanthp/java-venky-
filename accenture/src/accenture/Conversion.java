package accenture;

public class Conversion {

	public static void main(String[] args) {
             char c='A';
             int z=c;
             System.out.println(z);
             long l=1887;
             int f=(int)l;
             float g=100f;
             
             long h=(long)g;
             System.out.println(h);
             double d=100.23;
             int x=(int)d;
             System.out.println(x);
             int t=150;
             byte e=(byte)t;
             System.out.println(e);
             double k=383.1234;
             byte p=(byte)k;
             System.out.println(p); 
             
             
             
	}

}
