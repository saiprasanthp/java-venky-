//make a calculator using switch case

public class Exam4 {

	public static void main(String[] args) {
		int a=2;
		int b=2;
		int c=a*b;
		switch(c) {
		case 0:
			System.out.println("case 0");
			break;
		case 1:
			System.out.println("case 1");
			break;
		case 3:
			System.out.println("case 2");
			break;
		case 4:
			System.out.println("hello world!");
		}
	}

}
